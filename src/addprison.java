    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        String id = jTextField1.getText();
        String nm = jTextField2.getText();
        String st = jTextField4.getText();
        String nog = jTextField3.getText();
        String pin = jTextField5.getText();
        String ci = jTextField6.getText();
        try {
            if (nm.length() == 0 || id.length() == 0 || st.length() == 0 || jTextField3.getText().isEmpty() || pin.length() == 0 || ci.length() == 0) {
                JOptionPane.showMessageDialog(null, "Please enter all the fields");
                jTextField1.setText("");
                jTextField2.setText("");
                jTextField3.setText("");
                jTextField4.setText("");
                jTextField5.setText("");
                jTextField6.setText("");
            } else {
                int no = Integer.parseInt(jTextField3.getText());

                Class.forName("com.mysql.jdbc.Driver");
                Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/dbms?zeroDateTimeBehavior=convertToNull", "root", "123");
                PreparedStatement pstmt;
                int i = 0, j = 1;
                if (!(Pattern.matches("[a-zA-Z ]+", nm))) {
                    JOptionPane.showMessageDialog(null, "Name cannot have digits");
                    j = -1;

                }
                if (!(Pattern.matches("[a-zA-Z ]+", st))) {
                    JOptionPane.showMessageDialog(null, "State name cannot have digits");
                    j = -1;

                }
                if (!(Pattern.matches("[0-9 ]+", pin))) {
                    JOptionPane.showMessageDialog(null, "Pincode  cannot have alphabets");
                    j = -1;

                }
                if (!(Pattern.matches("[a-zA-Z ]+", ci))) {
                    JOptionPane.showMessageDialog(null, "City name cannot have digits");
                    j = -1;

                }
                if (id.length() != 5) {
                    j = -1;
                    JOptionPane.showMessageDialog(null, "Enter id of length 5");
                }

                if (pin.length() != 6) {
                    j = -1;
                    JOptionPane.showMessageDialog(null, "Enter pincode of length 6");
                }

                if (no > 9) {
                    j = -1;
                    JOptionPane.showMessageDialog(null, "Numbers of gates can't exceed 9");
                }
                if (j > 0) {
                    pstmt = con.prepareStatement("insert into prisondetailsa values(?,?,?,?,?,?)");
                    pstmt.setString(1, id);
                    pstmt.setString(2, nm);
                    pstmt.setInt(3, no);
                    pstmt.setString(4, st);
                    pstmt.setString(5, pin);
                    pstmt.setString(6, ci);

                    i = pstmt.executeUpdate();
                }

                if (i > 0 && j > 0) {
                    JOptionPane.showMessageDialog(null, "Addition Successful");
                    jTextField1.setText("");
                    jTextField2.setText("");
                    jTextField3.setText("");
                    jTextField4.setText("");
                    jTextField5.setText("");
                    jTextField6.setText("");

                } else {
                    JOptionPane.showMessageDialog(null, "Addition Failed");
                    jTextField1.setText("");
                    jTextField2.setText("");
                    jTextField3.setText("");
                    jTextField4.setText("");
                    jTextField5.setText("");
                    jTextField6.setText("");

                }

            }
        } catch (Exception e) {
            System.out.println(e);
            JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
            JOptionPane.showMessageDialog(null, "Addition Failed");
            jTextField1.setText("");
            jTextField2.setText("");
            jTextField3.setText("");
            jTextField4.setText("");
            jTextField5.setText("");
            jTextField6.setText("");

        }

    }//GEN-LAST:event_jButton2ActionPerformed


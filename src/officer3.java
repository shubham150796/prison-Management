    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            String id = jTextField1.getText();

            if (id.length() == 0) {

                JOptionPane.showMessageDialog(null, "Please enter  the id");
                jTextField1.setText("");

            } else {

                Class.forName("com.mysql.jdbc.Driver");
                Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/dbms?zeroDateTimeBehavior=convertToNull", "root", "123");
                Statement st = con.createStatement();

                String qry = "select * from courtvisit where PID='" + id + "'";
                ResultSet rs = st.executeQuery(qry);

                int flag = 0;

                //check if username exists or not in the table through while loop
                while (rs.next()) {
                    flag = 1;
                    String s = rs.getString(2);

                    jLabel5.setText(s + "");
                    s = rs.getString(3);
                    jLabel6.setText(s + "");

                }
                if (flag == 0) {
                    JOptionPane.showMessageDialog(null, "Invalid id");
                    jTextField1.setText("");
                }

            }
        } catch (Exception e) {
            System.out.println(e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed


    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String bw = jTextField1.getText();

        try {
            if (bw.length() == 0) {
                JOptionPane.showMessageDialog(null, "Please enter all the fields");
                jTextField1.setText("");
            } else {
                Class.forName("com.mysql.jdbc.Driver");
                Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/dbms?zeroDateTimeBehavior=convertToNull", "root", "123");
                int j = 0;
                if (bw.length() != 5) {
                    JOptionPane.showMessageDialog(null, "Enter valid block-wing");
                    j = -1;

                }
                PreparedStatement pstmt;
                int i = 0;
                if (j >= 0) {
                    pstmt = con.prepareStatement("insert into prison_architecture values(?)");
                    pstmt.setString(1, bw);
                    i = pstmt.executeUpdate();
                }

                if (i > 0 && j >= 0) {
                    JOptionPane.showMessageDialog(null, "Addition Successful");
                    jTextField1.setText("");

                } else if (i <= 0 || j < 0) {
                    JOptionPane.showMessageDialog(null, "Addition Failed");
                    jTextField1.setText("");

                }

            }
        } catch (Exception e) {
            System.out.println(e);
            JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
            JOptionPane.showMessageDialog(null, "Addition Failed");

            jTextField1.setText("");
        }
    }//GEN-LAST:event_jButton2ActionPerformed


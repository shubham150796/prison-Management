    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            String id = jTextField1.getText();
            if (id.length() == 0) {
                JOptionPane.showMessageDialog(null, "Please enter  the id");
                jTextField1.setText("");
            } else {
                Class.forName("com.mysql.jdbc.Driver");
                Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/dbms?zeroDateTimeBehavior=convertToNull", "root", "123");
                Statement st = con.createStatement();
                int flag1 = 0, flag = 0;
                String qry = "select * from prisoner where PID='" + id + "'";
                ResultSet rs = st.executeQuery(qry);
                while (rs.next()) {
                    flag1 = 1;
                }
                if (flag1 == 0) {
                    JOptionPane.showMessageDialog(null, "Invalid id");
                    jTextField1.setText("");
                }
                if (flag1 == 1) {
                    qry = "select dateOfVisit,purpose from courtvisit where PID='" + id + "'";

                    PreparedStatement pst = con.prepareStatement(qry);
                    rs = pst.executeQuery();
                    jTable1.setModel(DbUtils.resultSetToTableModel(rs));
                    rs.beforeFirst();
                    //check if username exists or not in the table through while loop
                    if (!rs.next()) {
                        JOptionPane.showMessageDialog(null, "This prisoner has no scheduled court visit");
                        jTextField1.setText("");
                    }
                }
            }
        } catch (Exception e) {
            System.out.println(e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed


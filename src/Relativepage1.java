    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        try {
            String id = jTextField1.getText();

            if (id.length() == 0) {

                JOptionPane.showMessageDialog(null, "Please enter  the id");
                jTextField1.setText("");

            } else {

                Class.forName("com.mysql.jdbc.Driver");
                Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/dbms?zeroDateTimeBehavior=convertToNull", "root", "123");
                Statement st = con.createStatement();
                String qry = "select * from prisoner where pid='" + id + "'";
                ResultSet rs = st.executeQuery(qry);
                System.out.println(rs);
                int flag = 0;
                //check if username exists or not in the table through while loop
                while (rs.next()) {
                    flag = 1;
                    String s = rs.getString(1);

                    jLabel9.setText(s + "");
                    s = rs.getString(3);
                    jLabel10.setText(s + "");
                    s = rs.getString(4);
                    jLabel12.setText(s + "");
                    s = rs.getString(6);
                    jLabel11.setText(s + "");
                    s = rs.getString(7);
                    jLabel14.setText(s + "");
                    s = rs.getString(8);
                    caseid = s;
                    jLabel13.setText(s + "");

                }
                if (flag == 0) {
                    JOptionPane.showMessageDialog(null, "Invalid id");
                    jTextField1.setText("");
                }

            }
        } catch (Exception e) {
            System.out.println(e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

